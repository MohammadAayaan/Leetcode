struct ListNode* mergeTwoLists(struct ListNode* l1, struct ListNode* l2){
    struct ListNode dummy;
    struct ListNode* tail = &dummy;
    dummy.next = NULL;

    while(l1 && l2){
        if(l1->val < l2->val){
            tail->next = l1;
            l1 = l1->next;
        } else {
            tail->next = l2;
            l2 = l2->next;
        }
        tail = tail->next;
    }

    if(l1) tail->next = l1;
    else tail->next = l2;

    return dummy.next;
}
